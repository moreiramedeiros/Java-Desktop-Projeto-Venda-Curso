    private void brnconsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnconsultaActionPerformed
        // Buscar venda por data

        try {
            //Converter data Br para data EUA
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate date_venda = LocalDate.parse(txtdata.getText(), formato);
            
            double total_venda;
            
            VendasDAO dao = new VendasDAO();
            total_venda = dao.retornaTotalVendaPorData(date_venda);
            
            txttotalvenda.setText(String.valueOf(total_venda));
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Informe uma data correspondente. \nErro :"+e);
        }


    }//GEN-LAST:event_brnconsultaActionPerformed

